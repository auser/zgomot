    def repeat(times, opts={})
      (1..times).to_a.inject(Midi::Channel.create(opts[:chan])) do |c,i|
        notes.kind_of?(Array) ? c + notes : c << notes
      end; self
    end

    #.........................................................................................................
    def time_shift(secs)
