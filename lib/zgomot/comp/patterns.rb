module Zgomot::Comp

  #####-------------------------------------------------------------------------------------------------------
  class Patterns

    #####-------------------------------------------------------------------------------------------------------
    class << self
    
      #.........................................................................................................
      def n(pc, oct=4, len=4, v=100)
        Midi::Note.new(:pitch => [pc, oct], :length => len, :velocity => v)
      end

      #.........................................................................................................
      def c(ps, len=4, v=100)
        ps.map{|p| Midi::Note.new(:pitch => p, :length => len, :velocity => v)}
      end

    #### self
    end

  #### Patterns
  end

#### Zgomot ::Comp
end
