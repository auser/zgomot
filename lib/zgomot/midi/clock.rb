module Zgomot::Midi

  #####-------------------------------------------------------------------------------------------------------
  class Clock

    #####-------------------------------------------------------------------------------------------------------
    class << self

      #.........................................................................................................
      attr_reader :measure, :note, :tick, :beat_note, :beats_per_measure, :beats_per_minute
    
      #.........................................................................................................
      def start
        @measure = 0
        @note = 0
        @tick = 0
        @beats_per_measure, @beat_note = Zgomot.config[:time_signature].split('/')
        @beats_per_minute = Zgomot.config[:beats_per_minute]
      end
      
      #.........................................................................................................
      def update(ticks=1)
      end

      #.........................................................................................................
      def to_s
        "#{measure}.#{note}.#{tick}"
      end

    #### self
    end

  #### Clock
  end

#### Zgomot ::Midi
end
